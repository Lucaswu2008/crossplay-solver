export function getBonusMultipliers(bonusCode) {
  switch (bonusCode) {
    case "2L":
      return { letter: 2, word: 1 };
    case "3L":
      return { letter: 3, word: 1 };
    case "4L":
      return { letter: 4, word: 1 };
    case "2W":
      return { letter: 1, word: 2 };
    case "3W":
      return { letter: 1, word: 3 };
    case "4W":
      return { letter: 1, word: 4 };
    default:
      return { letter: 1, word: 1 };
  }
}
